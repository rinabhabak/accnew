namespace Magedelight\Cybersourcesop\Observer;

use Magento\Framework\Event\Observer;
use Magento\Payment\Observer\AbstractDataAssignObserver;
use Magento\Quote\Api\Data\PaymentInterface;

/**
 * Class DataAssignObserver
 */
class DataAssignObserver extends AbstractDataAssignObserver
{
    const CC_TYPE = 'cc_type';
    const EXPIRATION = 'expiration';
    const EXPIRATION_YR = 'expiration_yr';
    const PUBLIC_HASH = 'public_hash';
    const CC_LAST_4 = 'cc_last_4';
    const IS_ACTIVE_PAYMENT_TOKEN_ENABLER = 'is_active_payment_token_enabler';

    /**
     * @var array
     */
    protected $additionalInformationList = [
        self::CC_TYPE,
        self::EXPIRATION,
        self::EXPIRATION_YR,
        self::IS_ACTIVE_PAYMENT_TOKEN_ENABLER
    ];

    /**
     * @param Observer $observer
     * @return void
     */
    public function execute(Observer $observer)
    {
        $data = $this->readDataArgument($observer);

        $additionalData = $data->getData(PaymentInterface::KEY_ADDITIONAL_DATA);
        if (!is_array($additionalData)) {
            return;
        }
        $paymentInfo = $this->readPaymentModelArgument($observer);
        foreach ($this->additionalInformationList as $additionalInformationKey) {
            if (isset($additionalData[$additionalInformationKey])) {
                $paymentInfo->setAdditionalInformation(
                    $additionalInformationKey,
                    $additionalData[$additionalInformationKey]
                );
            }
        }
    }
}
